// 2026-05-24 — 연생(태어난 해)별 풀이.
//   같은 띠라도 태어난 해의 천간(60갑자)이 달라 기운의 흐름이 조금씩 다름.
//   닭띠 파일럿: byYear 가 있으면 상세 페이지에서 ?birthYear=YYYY 로 연생 선택 노출.
//   - ganji/ganjiKo/element 은 정체성 표기용(한자/오행 라벨 허용),
//     summary/detail 본문은 naming-policy 에 따라 한글 + "X 기운" 표기만 사용.
export interface ZodiacByYearFortune {
  /** 천간×지지 한자 (정체성 표기, 본문 노출 금지) */
  ganji: string;
  /** 간지 한글 독음 (예: '정유') */
  ganjiKo: string;
  /** 천간 오행 한 줄 라벨 (예: '화 기운') */
  element: string;
  /** 연생 한 줄 요약 */
  summary: string;
  /** 연생 풀이 (3~4문장) */
  detail: string;
  /** 연생별 행동 조언 (1~2문장) */
  action: string;
}

// 2026-05-25 — 기간별 한 줄. 상세 페이지 기간 탭(오늘/이번주/이번달/올해)과
//   라벨이 일치하도록, 각 기간의 호흡에 맞춘 한 줄을 따로 둔다.
//   today=그날의 가벼운 포인트, week=짧은 호흡, month=중간 호흡, year=긴 호흡.
//   summary 는 다른 곳(메타/요약)에서 쓰일 수 있어 보존한다.
export interface ZodiacPeriodLines {
  today: string;
  week: string;
  month: string;
  year: string;
}

export interface ZodiacFortune {
  slug: string;
  label: string;
  years: string;
  summary: string;
  todayFocus: string;
  action: string;
  /** 기간별 한 줄(오늘/이번주/이번달/올해). 라벨과 내용 일치를 위해 분리. */
  periodLines: ZodiacPeriodLines;
  /** 기간별 집중 포인트(짧은 명사구). 기간 탭에 따라 바뀐다. */
  periodFocus: ZodiacPeriodLines;
  /** 기간별 행동 제안(한 문장). 기간 탭에 따라 바뀐다. */
  periodAction: ZodiacPeriodLines;
  /** 연생별 풀이 (연도 → 풀이). 없으면 단일 띠 화면 그대로. */
  byYear?: Record<number, ZodiacByYearFortune>;
}

