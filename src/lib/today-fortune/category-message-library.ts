// 변수 치환 ([이름] 등).
export function substituteCategoryVariables(template: string, vars: CategoryVariables): string {
  return template.replace(/\[이름\]/g, vars.name ?? '선생님');
}

/**
 * 카테고리 + 점수 → 등급별 풀에서 시드 기반 1문장 선택.
 * 같은 사용자·날짜는 동일한 메시지가 안정적으로 노출되도록 deterministic 시드 사용.
 */
export function pickCategoryMessage(
  category: CategoryKey,
  score: number,
  vars: CategoryVariables,
  seed: string
): string {
  const grade = scoreToGrade(score);
  const pool = CATEGORY_MESSAGE_LIBRARY[category][grade] ?? [];
  if (pool.length === 0) return '';
  // 해시 시드로 라운드-로빈 변형 선택.
  let hash = 0;
  const key = `${category}:${grade}:${seed}`;
  for (let i = 0; i < key.length; i += 1) {
    hash = (hash * 31 + key.charCodeAt(i)) >>> 0;
  }
  const template = pool[hash % pool.length]!;
  return substituteCategoryVariables(template, vars);
}
