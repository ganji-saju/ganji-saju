export type CategoryKey = 'wealth' | 'love' | 'career' | 'health' | 'relationship' | 'lottery';
export type GradeKey = 'peak' | 'good' | 'normal' | 'caution' | 'danger';

export interface CategoryVariables {
  name?: string;
  /** 사용자 상황 — 필터링·맞춤 카피용 (선택). */
  occupation?: 'salaried' | 'self_employed' | 'student' | 'freelancer' | null;
  relationship?: 'single' | 'dating' | 'married' | null;
  gender?: 'male' | 'female' | null;
}

export function scoreToGrade(score: number): GradeKey {
  if (score >= 90) return 'peak';
  if (score >= 70) return 'good';
  if (score >= 45) return 'normal';
  if (score >= 30) return 'caution';
  return 'danger';
}

