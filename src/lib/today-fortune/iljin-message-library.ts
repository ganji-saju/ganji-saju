// 변수 치환.
export interface MessageVariables {
  name?: string;
  element?: string;
  /** 시간대 (선택). */
  timeWindow?: string;
}

export function substituteVariables(template: string, vars: MessageVariables): string {
  return template
    .replace(/\[이름\]/g, vars.name ?? '선생님')
    .replace(/\[오행\]/g, vars.element ?? '')
    .replace(/\[시간대\]/g, vars.timeWindow ?? '')
    // 오행 등 placeholder 가 빈 문자열로 치환되면 생기는 이중 공백 정리.
    .replace(/ {2,}/g, ' ')
    .trim();
}

// 라운드-로빈: 같은 사용자가 같은 케이스를 봐도 매주 다른 변형 (week-of-year 기반).
export function pickVariant(caseId: IljinCaseId, seed: string): string {
  const variants = ILJIN_MESSAGE_LIBRARY[caseId];
  if (!variants || variants.length === 0) return '';
  let hash = 0;
  for (let i = 0; i < seed.length; i += 1) {
    hash = (hash * 31 + seed.charCodeAt(i)) >>> 0;
  }
  return variants[hash % variants.length]!;
}
