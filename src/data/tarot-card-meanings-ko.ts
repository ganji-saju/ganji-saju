export function getTarotCardMeaningKo(
  cardId: string,
  orientation: 'upright' | 'reversed'
): string | null {
  const entry = TAROT_CARD_MEANINGS_KO[cardId.toLowerCase()];
  if (!entry) return null;
  return orientation === 'reversed' ? entry.reversed : entry.upright;
}
