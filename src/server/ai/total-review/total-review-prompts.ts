export function buildSectionUserMessage(
  sectionId: TotalReviewSectionId,
  input: TotalReviewInput
): string {
  const lines: string[] = [];
  if (sectionId === 'main_narrative') {
    lines.push(MAIN_NARRATIVE_FEW_SHOT, '');
  }
  lines.push(
    SECTION_INSTRUCTIONS[sectionId],
    '',
    '## 입력 JSON',
    JSON.stringify(input, null, 2)
  );
  return lines.join('\n');
}
